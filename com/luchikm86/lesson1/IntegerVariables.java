package com.luchikm86.lesson1;

public class IntegerVariables {

    public static void main(String[] args) {
        int var1 = 10;
        int var2 = 15;
        int var3 = 33;

        System.out.println(sumVariable(var1,var2,var3));

    }

    public static int sumVariable (int a, int b, int c) {
        return a+b+c;
    }
}
